// MongoDB Operators -> powerfull

// Logical Operators
/*
  $inc => increment, +1, +2, +3,  -1, -2, -3..
  $min => minimum
  $max => maximum
  $set => used to set data
  $unset => removing property from an object
*/


// Array Operators
/*
  $push => add element at the end of an array
  $pop => used to extract/remove last element
  $pull => get the certain element
  $addToSet => add's the element to an array but doesn't allow duplications
*/
